package codeforces_marketing_scheme;

import java.util.Scanner;

public class Codeforces_Marketing_Scheme {
    public static void main(String[] args) {
        Scanner r=new Scanner(System.in);
        int u=r.nextInt();
        
        for(int i=0;i<u;i++)
        {
        double t=r.nextInt();
        
        
        
        double m=r.nextInt();
        
        
        
        double z=(m+1)/2;
        
        if(t>=z)
        {
            System.out.println("YES");
        }
        else
        {
            System.out.println("NO");
        }
        
        
    }
    
}
}