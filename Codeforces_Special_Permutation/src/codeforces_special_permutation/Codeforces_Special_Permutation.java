package codeforces_special_permutation;

import java.util.Scanner;

public class Codeforces_Special_Permutation {
    public static void main(String[] args) {
        Scanner r=new Scanner(System.in);
        
        int p=r.nextInt();
        for(int yh=0;yh<p;yh++)
        {
        int n=r.nextInt();
        
        
        System.out.print(n+" ");
        
        for(int i=1;i<=n-1;i++)
        {
          System.out.print(i+" ");
        }
        
        
        System.out.println();
        
        
    }
    
}
}